# ==================== ğŸ‡¨ğŸ‡¦ User Inputs ğŸ‡¨ğŸ‡¦ ====================
st.sidebar.header("ğŸ‡¨ğŸ‡¦ Simulation Settings")

sectors = ["Automotive", "Agriculture", "Manufacturing", "Energy", "Technology"]
