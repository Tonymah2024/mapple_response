# ==================== ğŸ” Predictive Economic Simulation ğŸ” ====================
st.markdown('<p class="sub-header">ğŸ“Š Predictive Economic Simulation</p>', unsafe_allow_html=True)

# Load or train AI model
model_file = "us_tariff_model.pkl"
if not os.path.exists(model_file):
    # Generate synthetic training data
    X_train = np.array([[x, y] for x in range(0, 51, 5) for y in range(0, 51, 5)])
    y_train = np.array([200 - (x * 3) + (y * 0.5) for x, y in X_train])

    model = LinearRegression()
    model.fit(X_train, y_train)
    joblib.dump(model, model_file)
else:
    model = joblib.load(model_file)

future_trade_volume = model.predict([[tariff_rate, subsidy_amount]])[0]
st.markdown(f'<p class="info-text">ğŸ“ˆ **Predicted Trade Volume in 5 Years:** {future_trade_volume:,.2f} Billion CAD</p>', unsafe_allow_html=True)

